package com.groupon.util.io;

/**
 * Objects of this type can be stored in a TSV file or in a database table etc.
 * The object simply returns an array of values that make it up along with an
 * array of bean property names.
 *
 * @author alasdair@groupon.com
 */
public interface ColumnSerializable {

    /**
     * Return an array of property names to use to serialize the instance.
     * There must be a corresponding setter and getter for each property.
     *
     * @return the properties to serialize.
     */
    public String[] getColumnProperties();
}
